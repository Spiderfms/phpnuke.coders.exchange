function translate($phrase) {
    switch($phrase) {
	case "xdatestring":	$tmp = "%A, %d-%b-%Y"; break;
	case "linksdatestring":	$tmp = "%d-%b-%Y"; break;
	case "xdatestring2":	$tmp = "%A, %d-%b-%Y"; break;
	default:		$tmp = "$phrase"; break;
    }
    return $tmp;
}
